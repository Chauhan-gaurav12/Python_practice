# range()--- Range function in the python is used to generate a sequence of number  
# --- we can also specify the start ,stop size as follow in range start size is optional 

for i in range(4): # By default range is start from the 0

    print(i)

for i in range(1,4):
    print(i)

for i in range(1,100,10): # In range we can give step size mins increment and decrement in this step soze is 10
    print(i)